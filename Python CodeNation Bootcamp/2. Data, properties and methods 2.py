print("hello, with all characters in upper case".upper())
# the .upper() changes the lower case text into UPPER CASE

print("HELLO, WITH ALL CHARACTERS IN LOWER CASE".lower())
# the .lower() changes the lower case text into lower case

print("hello, with the first character in upper case and all the next ones in lower case".capitalize())
# the .capitalize() method changes the first letter of a sentence to Capital

print("banana apple banana".count("banana"))
#.count() returns the amount of times a specific value is used in the string

print("hello, can you find the word banana?".find("banana"))
# The find() method finds the first occurrence of the specified value.
# The find() method returns -1 if the value is not found.

print("hello, can you replace the word black".replace("hello, can you replace the word black", "Olá, você poderia substituir a palavra preto"))
# The replace() method replaces a specified word or entire phrase with another specified word or phrase.

print("             bananas         apples       kiwis".strip())
#The strip() method removes any leading (spaces at the beginning) and trailing (spaces at the end) characters (space is the default leading character to remove)
