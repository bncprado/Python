"""
Demonstrate your understanding of the arithmetic operators.

Create a program which accepts two inputs from a user (num1 and num2), use these inputs with each operator.

Print the equation and the output.

"""

num1 = int(input('Type a the first number: '))
num2 = int(input('Now, type the second number: '))
print(num1+num2)
print(num1-num2)
print(num1/num2)
print(num1*num2)
print(num1**num2)
print(num1%num2)